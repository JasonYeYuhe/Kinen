    // MARK: - Tag Deduplication

    func testTagDeduplicationOnRestore() throws {
        let sharedTag = Tag(name: "shared-tag", colorHex: "#0000FF", isAutoGenerated: false)
        context.insert(sharedTag)
        let entry = JournalEntry(content: "Entry with existing tag")
        entry.addTag(sharedTag)
        context.insert(entry)
        try context.save()

        let entries = try context.fetch(FetchDescriptor<JournalEntry>())
        let tags = try context.fetch(FetchDescriptor<Tag>())
        let encrypted = try BackupService.createBackup(entries: entries, tags: tags, password: "pass")

        // Restore into context that already has the same tag
        let restoreConfig = ModelConfiguration(schema: container.schema, isStoredInMemoryOnly: true)
        let restoreContainer = try ModelContainer(for: container.schema, configurations: [restoreConfig])
        let restoreContext = ModelContext(restoreContainer)

        // Pre-insert the tag so restoreBackup finds it already exists
        let preExistingTag = Tag(name: "shared-tag", colorHex: "#FF0000", isAutoGenerated: false)
        restoreContext.insert(preExistingTag)
        try restoreContext.save()

        _ = try BackupService.restoreBackup(data: encrypted, password: "pass", context: restoreContext)
        try restoreContext.save()

        let allTags = try restoreContext.fetch(FetchDescriptor<Tag>())
        XCTAssertEqual(allTags.count, 1, "Should not create a duplicate tag that already exists in context")
    }

